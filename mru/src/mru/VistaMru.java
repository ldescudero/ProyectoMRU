    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
       
        double resultado=0.0;
       double velocidad=0.0;
       lblCarro.setLocation(400, lblCarro.getY());
       
      TimerTask timerTask = new TimerTask() 
     { 
         public void run()  
         { 
           if(lblCarro.getX()<=700){
               btnCalcular.setEnabled(false);
             lblCarro.setLocation(lblCarro.getX()+10, lblCarro.getY());
              
           }else{    
                btnCalcular.setEnabled(true);
           timer.cancel();
           }
         
         } 
     };
    
        if(txtDistancia.isEnabled()==false){
           velocidad=Double.parseDouble(txtVelocidad.getText());
           resultado=mru.calcularDistancia( Double.parseDouble(txtTiempo.getText()),velocidad);
           lblResultado.setText(""+resultado+" metros");
       }
          if(txtVelocidad.isEnabled()==false){
          
           resultado=mru.calcularVelocidad( Double.parseDouble(txtDistancia.getText()),Double.parseDouble(txtTiempo.getText()));
           velocidad=resultado;
           lblResultado.setText(""+resultado+" metros/segundos");
       }
            if(txtTiempo.isEnabled()==false){
            velocidad=Double.parseDouble(txtVelocidad.getText());
           resultado=mru.calcularTiempo( Double.parseDouble(txtDistancia.getText()),velocidad);
        
           lblResultado.setText(""+resultado+" segundos");
       }
             timer=new Timer();  
     timer.scheduleAtFixedRate(timerTask, 0, Math.abs((int)(1000/velocidad)));
      
           
    }//GEN-LAST:event_btnCalcularActionPerformed
