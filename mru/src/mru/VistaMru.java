    private void txtVelocidadKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtVelocidadKeyTyped
         char c=evt.getKeyChar(); 
             
         
          if(Character.isDigit(c)==false) { 
              
               if(c!='.'){
                    evt.consume(); 
                getToolkit().beep();
               }
               
          } 
    }//GEN-LAST:event_txtVelocidadKeyTyped

